def solveMaze(maze):
    
    wasHere=[[False for x in range(0,12)] for y in range(0,12)]    
    correctPath=[[False for x in range(0,12)] for y in range(0,12)]    

    b, Path = recursiveSolve(1, 1, wasHere, correctPath, maze)

    return b, Path


def recursiveSolve(x,y,wasHere,correctPath,maze):
    if (x == 10 & y == 10):
        correctPath[x][y] = True;
        return True, correctPath 
        
    if (maze[x][y] == 1 | wasHere[x][y]):
        return False, correctPath  
        
    wasHere[x][y] = True;

    if (maze[x-1][y] == 0 | wasHere[x-1][y]==False):
        b,correctPath=recursiveSolve(x-1, y,wasHere,correctPath,maze)
        if (b):
            correctPath[x][y] = True 
            return True, correctPath
            
    if (maze[x+1][y] == 0 | wasHere[x+1][y]==False):
        b,correctPath=recursiveSolve(x+1, y,wasHere,correctPath,maze)
        if (b): 
            correctPath[x][y] = True
            return True, correctPath
    
    if (maze[x][y-1] == 0 | wasHere[x][y-1]==False):
        b,correctPath=recursiveSolve(x, y-1,wasHere,correctPath,maze)
        if (b): 
            correctPath[x][y] = True
            return True, correctPath
    
    if (maze[x][y+1] == 0 | wasHere[x][y+1]==False):
        b,correctPath=recursiveSolve(x, y+1,wasHere,correctPath,maze)
        if (b): 
            correctPath[x][y] = True
            return True, correctPath
        
    return False, correctPath



def genPathString(Path):
    
    wasHere=[[False for x in range(0,12)] for y in range(0,12)]
    
    pathString = recursivegenPath("",wasHere,Path,1,1)

    return pathString

def recursivegenPath(pathString,wasHere,Path,x,y):
    
    wasHere[x][y]=True
    
    if ((Path[x-1][y]==True) & (wasHere[x-1][y]==False)):
        pathString=pathString + "N"

        pathString=recursivegenPath(pathString,wasHere,Path,x-1,y)
        return pathString
        
    if ((Path[x+1][y]==True) & (wasHere[x+1][y]==False)):
        pathString=pathString + "S"
        pathString=recursivegenPath(pathString,wasHere,Path,x+1,y)
        return pathString
        
    if ((Path[x][y-1]==True) & (wasHere[x][y-1]==False)):
        pathString=pathString + "W"
        pathString=recursivegenPath(pathString,wasHere,Path,x,y-1)
        return pathString
        
    if ((Path[x][y+1]==True) & (wasHere[x][y+1]==False)):
        pathString=pathString + "E"
        pathString=recursivegenPath(pathString,wasHere,Path,x,y+1)
        return pathString

    return pathString         


